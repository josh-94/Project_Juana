CREATE TABLE productos(
    numeroGuia VARCHAR(25) NULL,
    numeroPedido VARCHAR(25) NULL,
    estado VARCHAR(25) NULL,
    lugar VARCHAR(25) NULL,
    quienRecibe VARCHAR(25) NULL,
    motivoDescripcion VARCHAR(25) NULL,
    fecha VARCHAR(25) NULL,
    hora VARCHAR(25) NULL,
    link VARCHAR(25) NULL,
    observacion VARCHAR(25) NULL,
    eliminado TINYINT(1) NOT NULL
)
